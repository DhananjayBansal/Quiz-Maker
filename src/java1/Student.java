    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().equals(""))
           errjLabel.setText("Enter student name");
        else
        {
        cobj.Connect();
        try
        {  
        pst=cobj.con.prepareStatement("Select * from student1 where Studentname=?");
        pst.setString(1,jTextField1.getText());
        rst= pst.executeQuery();
                if(rst.next())
                {
                    errjLabel.setText("Record Found");
                    id = rst.getLong(1);
                    jTextField1.setText(rst.getString(2).trim());
                    jTextField2.setText(rst.getString(3).trim());
                    jTextField3.setText(rst.getString(4).trim());
                    jTextField4.setText(rst.getString(5).trim());
                    jTextField5.setText(rst.getString(6).trim());
                    jTextField6.setText(rst.getString(7).trim());
                }
                else{
                    errjLabel.setText("Not Found");
               }
                rst.close();
        }
        catch(Exception ex)
        {System.out.print(ex);
        }
        cobj.DisConnect();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

