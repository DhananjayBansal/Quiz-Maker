    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         cobj.Connect();
        try
        {
        pst = cobj.con.prepareCall("{call prcinsertStudent(?,?,?,?,?,?)}");
        pst.setString(1, jTextField1.getText());
        pst.setString(2, jTextField2.getText());
        pst.setString(3, jTextField3.getText());
        pst.setString(4, jTextField4.getText());
        pst.setInt(5, Integer.parseInt(jTextField5.getText()));
        pst.setDouble(6, Double.parseDouble(jTextField6.getText()));
        if(pst.execute()==false)       
            if(pst.getUpdateCount()>0)
            {
            errjLabel.setText("Record Saved");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            }
        }
        catch(Exception ex)
        {
        System.out.print("Error in save"+ ex);
        }
        cobj.DisConnect();
    }//GEN-LAST:event_jButton1ActionPerformed

